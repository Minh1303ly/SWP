package modal;

import java.util.Date;

/**
 *
 * @author Đạt
 */
public class Rating {
    private int id;
    private int productId;
    private int userId;
    private int rating;
    private String comment;
    private Date createdAt;
    private Date modifiedAt;

    public Rating() {
    }

    @Override
    public String toString() {
        return "Rating{" + "id=" + id + ", productId=" + productId + ", userId=" + userId + ", rating=" + rating + ", comment=" + comment + ", createdAt=" + createdAt + ", modifiedAt=" + modifiedAt + '}';
    }

    public Rating(int id, int productId, int userId, int rating, String comment, Date createdAt, Date modifiedAt) {
        this.id = id;
        this.productId = productId;
        this.userId = userId;
        this.rating = rating;
        this.comment = comment;
        this.createdAt = createdAt;
        this.modifiedAt = modifiedAt;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getProductId() {
        return productId;
    }

    public void setProductId(int productId) {
        this.productId = productId;
    }

    public int getUserId() {
        return userId;
    }

    public void setUserId(int userId) {
        this.userId = userId;
    }

    public int getRating() {
        return rating;
    }

    public void setRating(int rating) {
        this.rating = rating;
    }

    public String getComment() {
        return comment;
    }

    public void setComment(String comment) {
        this.comment = comment;
    }

    public Date getCreatedAt() {
        return createdAt;
    }

    public void setCreatedAt(Date createdAt) {
        this.createdAt = createdAt;
    }

    public Date getModifiedAt() {
        return modifiedAt;
    }

    public void setModifiedAt(Date modifiedAt) {
        this.modifiedAt = modifiedAt;
    }
    
}
